        # 超大数字串逐位取模（避免溢出）
        def string_mod(str_num: str, p: int) -> int:
            res = 0
            for c in str_num:
                digit = int(c)
                res = (res * 10 + digit) % p
            return res

        # 生成遍历序列
        def generate_traversal_sequence(struct: List[int], values: List[int]) -> str:
            seq = []
            leaf_idx = 0
            stack = [(0, False)]  # (节点索引, 是否已访问)

            while stack:
                node_idx, visited = stack.pop()
                if not visited:
                    seq.append("1")
                    stack.append((node_idx, True))
                    if struct[node_idx] == 1:
                        seq.append(str(values[leaf_idx]))
                        leaf_idx += 1
                    else:
                        left = 2 * node_idx + 1
                        right = 2 * node_idx + 2
                        if right < len(struct):
                            stack.append((right, False))
                        if left < len(struct):
                            stack.append((left, False))
                else:
                    seq.append("9")
            return "".join(seq)

