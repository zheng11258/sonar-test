        for struct in structures:
            # 枚举所有宝石排列（区分编号）
            for perm in itertools.permutations(gem):
                num_str = generate_traversal_sequence(struct, list(perm))
                mod_res = string_mod(num_str, p)
                if mod_res == target:
                    count += 1

        return count

